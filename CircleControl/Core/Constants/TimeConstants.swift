import UIKit

struct TimeConstants {
    static let minutesPerDay: CGFloat = 24 * 60
    static let radiansPerMinute: CGFloat = (2 * .pi) / minutesPerDay
    static let radiansPerHour: CGFloat = .pi / 12
    static let offsetMinutes: CGFloat = 18 * 60
}
